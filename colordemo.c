#include <stdio.h>
int main() {
    // ANSI escape codes for text colors
    printf("\033[1;31mRed text\033[0m\n");      // Red text
    printf("\033[1;32mGreen text\033[0m\n");    // Green text
    printf("\033[1;33mYellow text\033[0m\n");   // Yellow text
    printf("\033[1;34mBlue text\033[0m\n");     // Blue text
    printf("\033[1;35mMagenta text\033[0m\n");  // Magenta text
    printf("\033[1;36mCyan text\033[0m\n");     // Cyan text

}
